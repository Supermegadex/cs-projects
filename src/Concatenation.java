public class Concatenation {
  public static void main(String[] args) {
    int luckyNumber = 2;
    double pi = 3.14;
    String favoriteSubject = "Basket Weaving";
    String name = "Daniel";
    int gradYear = 2019;

    System.out.println("My name is " + name + " and I will graduate in " + gradYear + ".");
    System.out.println("My favorite subject is " + favoriteSubject + ".");
    System.out.println("Pi (" + pi + ") is pretty cool.");
    System.out.println("My lucky number is " + luckyNumber + ".");
  }
}